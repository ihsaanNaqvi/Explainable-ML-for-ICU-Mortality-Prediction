"""
src/update_thesis_figures.py — Replace thesis figures in thesis.docx with
publication-quality versions generated by the fix_*.py scripts.

Strategy
--------
Phase 1 — REPLACE (exact caption match):
    Scan all Image Caption paragraphs for "Figure X.Y" prefix.
    Clear the preceding Captioned Figure paragraph, insert new PNG at 15 cm, centred.

Phase 2 — REMAP (content-aware replacement for renumbered figures):
    The compiled docx has old figure numbers that differ from the target numbering.
    e.g. old "Figure 3.3" (SHAP heatmap) -> replaced with fig5_4_shap_heatmap.png
         old "Figure 3.5" (concordance scatter) -> replaced with fig6_2_concordance_vs_calibration.png
    Image is replaced; original caption text is kept unchanged.

Phase 3 — INSERT (figures missing from docx entirely):
    Remaining figures are inserted into Appendix D ("Figure pack") which already
    exists in the document.  Each block: centred image + centred caption.
    The user should cut-and-paste them to the correct chapter location.

Usage
-----
    python src/update_thesis_figures.py
Output: thesis/thesis_FINAL_updated.docx

Note on filename corrections vs user specification
---------------------------------------------------
The user's prompt used:
  "fig3_2_distributions.png"     — actual file: fig3_2_variable_distributions.png
  "fig6_2_concordance_scatter.png" — actual file: fig6_2_concordance_vs_calibration.png
This script uses the actual on-disk filenames.
"""
from pathlib import Path
from docx import Document
from docx.shared import Cm
from docx.enum.text import WD_ALIGN_PARAGRAPH
from docx.oxml.ns import qn
import re

ROOT      = Path(__file__).resolve().parents[1]
DOCX_IN   = ROOT / "thesis" / "thesis.docx"
DOCX_OUT  = ROOT / "thesis" / "thesis_FINAL_updated.docx"
FIG_DIR   = ROOT / "outputs" / "figures" / "thesis"

# ── Figure replacement map ────────────────────────────────────────────────
# caption_prefix -> png filename (actual on-disk names)
CAPTION_TO_FILE = {
    "Figure 2.1": "fig2_1_missingness_FIXED.png",
    "Figure 2.2": "fig2_2_transformer_FIXED.png",
    "Figure 3.1": "fig3_1_class_imbalance.png",
    "Figure 3.2": "fig3_2_variable_distributions.png",
    "Figure 4.1": "fig4_1_training_curves.png",
    "Figure 5.1": "fig5_1_roc_curves.png",
    "Figure 5.2": "fig5_2_pr_curves.png",
    "Figure 5.3": "fig5_3_shap_global.png",
    "Figure 5.4": "fig5_4_shap_heatmap.png",
    "Figure 6.1": "fig6_1_concordance_dist.png",
    "Figure 6.2": "fig6_2_concordance_vs_calibration.png",
}

# Old caption prefix in docx -> target figure key in CAPTION_TO_FILE
# These are figures that exist in the compiled docx under older chapter numbering.
REMAP_OLD_TO_NEW = {
    "Figure 3.3": "Figure 5.4",   # SHAP time-step heatmap
    "Figure 3.5": "Figure 6.2",   # SHAP–SOFA concordance scatter
}

# Caption text to attach when inserting missing figures into Appendix D
INSERT_CAPTIONS = {
    "Figure 3.2": "Figure 3.2 — Variable distributions by outcome (KDE). "
                  "GCS, Lactate, MAP, and Creatinine shown for survivors vs non-survivors.",
    "Figure 4.1": "Figure 4.1 — Training and validation curves for TCN and Transformer. "
                  "Loss (focal) and AUPRC shown per epoch; best epoch marked.",
    "Figure 5.1": "Figure 5.1 — ROC curves comparison: XGBoost, TCN, Transformer, SAPS-I. "
                  "Shaded area = ±1 SD bootstrap CI.",
    "Figure 5.2": "Figure 5.2 — Precision-Recall curves comparison. "
                  "Dashed baseline = random classifier at class prevalence 13.5%.",
    "Figure 5.3": "Figure 5.3 — SHAP global feature importance (top 15 variables). "
                  "Mean |SHAP| across 600 test patients; colour encodes median feature value.",
    "Figure 5.4": "Figure 5.4 — Time-step SHAP attribution heatmap. "
                  "Case study patient (non-survivor); top-10 variables by total |SHAP|.",
    "Figure 6.1": "Figure 6.1 — Distribution of SHAP–SOFA concordance scores. "
                  "C(p) = |Top5(p) ∩ SOFA vars| / 5 across 600 test patients.",
    "Figure 6.2": "Figure 6.2 — SOFA concordance vs patient-level calibration error. "
                  "Spearman ρ = −0.051 (p = 0.215, n.s.)  |  ICU types colour-coded.",
}


# ── Helpers ──────────────────────────────────────────────────────────────

def _verify_files():
    missing = []
    for key, fname in CAPTION_TO_FILE.items():
        path = FIG_DIR / fname
        if not path.exists():
            missing.append(f"  MISSING: {fname}  (for {key})")
    if missing:
        print("WARNING — the following figure files were not found:")
        for m in missing:
            print(m)
    return {k: FIG_DIR / v for k, v in CAPTION_TO_FILE.items()
            if (FIG_DIR / v).exists()}


def _caption_key(text: str):
    """Return 'Figure X.Y' prefix if text starts with one, else None."""
    m = re.match(r"(Figure\s+\d+\.\d+)", text.strip())
    return m.group(1) if m else None


def _replace_image_in_para(para, img_path: Path, width_cm: float = 15.0):
    """Remove all runs from para and insert a single centred picture."""
    p_elem = para._p
    for r in list(p_elem.findall(qn("w:r"))):
        p_elem.remove(r)
    run = para.add_run()
    run.add_picture(str(img_path), width=Cm(width_cm))
    para.alignment = WD_ALIGN_PARAGRAPH.CENTER


def _find_appendix_d_para(doc) -> int:
    """Return paragraph index of the Appendix D heading, or -1 if not found."""
    for i, para in enumerate(doc.paragraphs):
        if "Appendix D" in para.text and "Figure" in para.text:
            return i
    # Fallback: find any Appendix D heading
    for i, para in enumerate(doc.paragraphs):
        if re.search(r"Appendix\s+D", para.text, re.IGNORECASE):
            return i
    return -1


def _insert_figure_block(after_para, img_path: Path, caption_text: str,
                          doc, width_cm: float = 15.0):
    """
    Insert a Captioned Figure + Image Caption block immediately after after_para.
    Uses add_paragraph() + XML reorder to avoid constructor complexity.
    Returns (fig_para, cap_para).
    """
    # Build image paragraph
    fig_para = doc.add_paragraph()
    try:
        fig_para.style = doc.styles["Captioned Figure"]
    except KeyError:
        pass
    fig_para.alignment = WD_ALIGN_PARAGRAPH.CENTER
    fig_para.add_run().add_picture(str(img_path), width=Cm(width_cm))

    # Build caption paragraph
    cap_para = doc.add_paragraph()
    try:
        cap_para.style = doc.styles["Image Caption"]
    except KeyError:
        pass
    cap_para.alignment = WD_ALIGN_PARAGRAPH.CENTER
    cap_para.add_run(caption_text)

    # Both were appended at the end of body; move them to after after_para
    body = doc.element.body
    body.remove(fig_para._p)
    body.remove(cap_para._p)
    after_para._p.addnext(cap_para._p)   # insert cap after anchor
    after_para._p.addnext(fig_para._p)   # insert fig before cap (it now sits between)

    return fig_para, cap_para


# ── Main ─────────────────────────────────────────────────────────────────

def main():
    print(f"\nLoading  {DOCX_IN} ...")
    doc = Document(str(DOCX_IN))

    file_map = _verify_files()   # key -> resolved Path
    if not file_map:
        print("ERROR: no figure files found in", FIG_DIR)
        return

    paras = doc.paragraphs
    n     = len(paras)
    replaced  = []
    remapped  = []
    not_found = []

    # ── Phase 1: replace by exact caption prefix ─────────────────────────
    remaining = set(CAPTION_TO_FILE.keys())

    for i, para in enumerate(paras):
        if para.style.name != "Image Caption":
            continue
        key = _caption_key(para.text)
        if key not in remaining:
            continue
        if key not in file_map:
            not_found.append(key)
            remaining.discard(key)
            continue

        # Preceding paragraph should be the Captioned Figure
        if i == 0:
            print(f"  SKIP {key}: caption is first paragraph (no preceding image para)")
            continue
        fig_para = paras[i - 1]
        has_img  = fig_para._p.find(".//" + qn("w:drawing")) is not None
        if not has_img:
            print(f"  WARN {key}: preceding para has no drawing — attempting replacement anyway")

        _replace_image_in_para(fig_para, file_map[key])
        replaced.append(key)
        remaining.discard(key)
        print(f"  [replaced] {key}  ->  {file_map[key].name}")

    # ── Phase 2: remap old caption numbers to new files ──────────────────
    # Refresh paras list after potential XML modification
    paras = doc.paragraphs

    for i, para in enumerate(paras):
        if para.style.name != "Image Caption":
            continue
        key = _caption_key(para.text)
        if key not in REMAP_OLD_TO_NEW:
            continue
        new_key = REMAP_OLD_TO_NEW[key]
        if new_key not in file_map:
            print(f"  SKIP remap {key}->{new_key}: file not found")
            continue

        if i == 0:
            continue
        fig_para = paras[i - 1]
        _replace_image_in_para(fig_para, file_map[new_key])
        remapped.append((key, new_key))
        remaining.discard(new_key)
        print(f"  [remapped] {key}  ->  {new_key}  ({file_map[new_key].name})")

    # ── Phase 3: insert missing figures into Appendix D ──────────────────
    still_missing = [k for k in remaining if k in file_map]

    if still_missing:
        app_d_idx = _find_appendix_d_para(doc)
        paras = doc.paragraphs   # refresh

        if app_d_idx < 0:
            print("  WARN: Appendix D not found — appending at end of document")
            anchor = paras[-1]
        else:
            # Find the last paragraph of Appendix D content
            # (everything before the next Appendix heading or end of doc)
            anchor = paras[app_d_idx]
            for j in range(app_d_idx + 1, len(paras)):
                p = paras[j]
                if p.style.name.startswith("Heading") and \
                        re.search(r"Appendix\s+[E-Z]", p.text, re.IGNORECASE):
                    break
                anchor = p

        # Insert a note paragraph first (only once)
        note_para = doc.add_paragraph(
            "[PLACEMENT NOTE] The figures below were not present in the compiled docx. "
            "Cut and paste each block to its correct chapter location."
        )
        note_para.style = doc.styles["Body Text"] if "Body Text" in \
            [s.name for s in doc.styles] else doc.styles["Normal"]
        body = doc.element.body
        body.remove(note_para._p)
        anchor._p.addnext(note_para._p)
        anchor = note_para

        for key in sorted(still_missing):
            caption_text = INSERT_CAPTIONS.get(key, f"{key} — [caption not specified]")
            _insert_figure_block(anchor, file_map[key], caption_text, doc)
            # Advance anchor to the just-inserted caption paragraph
            # (it was inserted after anchor, so find it in the refreshed list)
            paras = doc.paragraphs
            for j, p in enumerate(paras):
                if p.text == caption_text:
                    anchor = p
                    break
            not_found.append(key)
            print(f"  [inserted->AppD] {key}  ->  {file_map[key].name}")

    # ── Save ─────────────────────────────────────────────────────────────
    doc.save(str(DOCX_OUT))
    print(f"\nSaved  ->  {DOCX_OUT}")

    # ── Summary ──────────────────────────────────────────────────────────
    print("\n" + "=" * 60)
    print("FIGURE UPDATE SUMMARY")
    print("=" * 60)

    if replaced:
        print(f"\nReplaced ({len(replaced)} figures — image swapped, caption kept):")
        for k in replaced:
            print(f"  {k:12s}  {CAPTION_TO_FILE[k]}")

    if remapped:
        print(f"\nRemapped ({len(remapped)} figures — old number -> new file, caption kept):")
        for old_k, new_k in remapped:
            print(f"  {old_k:12s} -> {new_k:12s}  {CAPTION_TO_FILE[new_k]}")

    skipped_insert = [k for k in not_found if k not in still_missing]
    if skipped_insert:
        print(f"\nSkipped (file missing on disk):")
        for k in skipped_insert:
            print(f"  {k}")

    if still_missing:
        print(f"\nInserted into Appendix D ({len(still_missing)} figures — move to chapter):")
        for k in sorted(still_missing):
            print(f"  {k:12s}  {CAPTION_TO_FILE[k]}")

    total_done = len(replaced) + len(remapped) + len(still_missing)
    print(f"\nTotal figures processed: {total_done} / {len(CAPTION_TO_FILE)}")
    print("=" * 60)


if __name__ == "__main__":
    main()
