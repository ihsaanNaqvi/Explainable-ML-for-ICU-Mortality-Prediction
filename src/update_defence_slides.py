"""
src/update_defence_slides.py — Replace thesis defence figures with
publication-quality versions generated by the fix_*.py scripts.

Keyword matching strategy
--------------------------
Each target figure is associated with a regex pattern (case-insensitive).
The first slide whose concatenated text matches the pattern gets that figure.
Slides are processed in keyword-definition order to avoid ambiguity
(e.g. slide 9 is claimed by 'shap' before 'concordance' is tested).

Known behaviour
---------------
* 'roc' alone does not appear in any slide text, so 'roc|comparison'
  is used — matching slide 8 "Results — Model Comparison".
* 'training' does not appear as a standalone word in any slide; fig4_1
  is reported as unmatched and skipped.  Add slide number to
  SLIDE_OVERRIDES to place it manually, e.g. SLIDE_OVERRIDES['training'] = 8.
* Slides 1 (ECG strip), 11 (novelty visual) and 12 (QR codes) contain
  images that do NOT match any keyword and are left unchanged.

Usage
-----
    python src/update_defence_slides.py
Output: thesis/defence_presentation_FINAL.pptx

Filename note
-------------
User spec used "fig6_2_concordance_scatter.png"; actual file on disk is
fig6_2_concordance_vs_calibration.png — this script uses the correct name.
"""
from __future__ import annotations
import re, sys, io
from pathlib import Path
from collections import OrderedDict
from PIL import Image as PILImage
from pptx import Presentation
from pptx.util import Inches, Emu
from pptx.enum.shapes import MSO_SHAPE_TYPE

# Force UTF-8 console output on Windows
sys.stdout = io.TextIOWrapper(sys.stdout.buffer, encoding="utf-8", errors="replace")

ROOT    = Path(__file__).resolve().parents[1]
PPTX_IN  = ROOT / "thesis" / "defence_presentation.pptx"
PPTX_OUT = ROOT / "thesis" / "defence_presentation_FINAL.pptx"
FIG_DIR  = ROOT / "outputs" / "figures" / "thesis"

# ── Keyword pattern -> figure filename (OrderedDict = processing order matters) ──
# Patterns are matched against ALL text on each slide (case-insensitive).
KEYWORD_MAP: OrderedDict[str, str] = OrderedDict([
    (r"missingness",         "fig2_1_missingness_FIXED.png"),
    (r"transformer|architecture", "fig2_2_transformer_FIXED.png"),
    (r"roc|comparison",      "fig5_1_roc_curves.png"),
    (r"heatmap",             "fig5_4_shap_heatmap.png"),
    (r"concordance",         "fig6_2_concordance_vs_calibration.png"),
    (r"\btraining\b",        "fig4_1_training_curves.png"),
])

# Optional hard overrides: keyword -> 1-indexed slide number.
# Set to None to skip.  Used when keyword is absent from slide text.
SLIDE_OVERRIDES: dict[str, int | None] = {
    # "training": 8,   # uncomment to force training curves onto slide 8
}

# Minimum image dimensions to qualify as a "content image" (not an icon/logo)
MIN_IMG_W = Inches(2.0)
MIN_IMG_H = Inches(1.5)

# Bottom / right safety margin — new image will not extend beyond these
MARGIN = Inches(0.15)


# ── Helpers ──────────────────────────────────────────────────────────────

def slide_text(slide) -> str:
    """Concatenate all text from every shape on the slide."""
    parts = []
    for shape in slide.shapes:
        if shape.has_text_frame:
            parts.append(shape.text_frame.text)
    return " ".join(parts)


def main_content_image(slide):
    """
    Return the largest Picture shape (by pixel area) whose dimensions
    exceed MIN_IMG_W x MIN_IMG_H.  Returns None if no qualifying shape.
    """
    candidates = [
        s for s in slide.shapes
        if s.shape_type == MSO_SHAPE_TYPE.PICTURE
        and s.width  >= MIN_IMG_W
        and s.height >= MIN_IMG_H
    ]
    if not candidates:
        return None
    return max(candidates, key=lambda s: s.width * s.height)


def fit_image(img_path: Path,
              anchor_left, anchor_top,
              max_width, max_height
              ) -> tuple[int, int, int, int]:
    """
    Compute (left, top, width, height) in EMU so the image fits
    inside (max_width x max_height) while preserving aspect ratio.
    The anchor position is kept; the image may be narrower than max_width
    if it is portrait-oriented.
    """
    with PILImage.open(img_path) as img:
        px_w, px_h = img.size

    aspect = px_h / px_w   # > 1 for portrait, < 1 for landscape

    # Try width-limited first
    if int(max_width * aspect) <= max_height:
        new_w = max_width
        new_h = int(max_width * aspect)
    else:
        # Height-limited
        new_h = max_height
        new_w = int(max_height / aspect)

    return int(anchor_left), int(anchor_top), int(new_w), int(new_h)


def remove_shape(shape):
    """Remove a shape element from its parent XML tree."""
    sp = shape._element
    sp.getparent().remove(sp)


# ── Main ─────────────────────────────────────────────────────────────────

def main():
    print(f"\nLoading  {PPTX_IN} ...")
    prs = Presentation(str(PPTX_IN))

    slide_w = prs.slide_width
    slide_h = prs.slide_height
    print(f"Slide dimensions: {slide_w.inches:.2f}\" x {slide_h.inches:.2f}\"")
    print(f"Total slides:     {len(prs.slides)}")
    print()

    # ── Build keyword -> slide index mapping ─────────────────────────────
    # A slide can only be claimed by one keyword (first match wins).
    claimed_slides: set[int] = set()
    assignment: list[tuple[str, str, int]] = []   # (keyword, fig_file, slide_idx)
    unmatched:  list[tuple[str, str]]      = []   # (keyword, fig_file)

    slides_text = [slide_text(s) for s in prs.slides]

    for pattern, fig_file in KEYWORD_MAP.items():
        # Check SLIDE_OVERRIDES first
        short_key = pattern.split("|")[0].strip(r"\b")
        if short_key in SLIDE_OVERRIDES:
            override_num = SLIDE_OVERRIDES[short_key]
            if override_num is None:
                unmatched.append((pattern, fig_file))
                continue
            slide_idx = override_num - 1   # convert to 0-indexed
            if slide_idx in claimed_slides:
                print(f"  WARN: override slide {override_num} already claimed, skipping {fig_file}")
                unmatched.append((pattern, fig_file))
                continue
            claimed_slides.add(slide_idx)
            assignment.append((pattern, fig_file, slide_idx))
            continue

        # Keyword search (case-insensitive)
        matched_idx = None
        for idx, text in enumerate(slides_text):
            if idx in claimed_slides:
                continue
            if re.search(pattern, text, re.IGNORECASE):
                # Extra check: slide must have a qualifying content image
                if main_content_image(prs.slides[idx]) is not None:
                    matched_idx = idx
                    break

        if matched_idx is not None:
            claimed_slides.add(matched_idx)
            assignment.append((pattern, fig_file, matched_idx))
        else:
            unmatched.append((pattern, fig_file))

    # ── Print matching plan ───────────────────────────────────────────────
    print("Keyword -> slide matching:")
    for pattern, fig_file, slide_idx in assignment:
        print(f"  slide {slide_idx+1:2d}  [{pattern}]  ->  {fig_file}")
    for pattern, fig_file in unmatched:
        print(f"  NO MATCH  [{pattern}]  ({fig_file})")
    print()

    # ── Replace images ───────────────────────────────────────────────────
    replaced = []

    for pattern, fig_file, slide_idx in assignment:
        img_path = FIG_DIR / fig_file
        if not img_path.exists():
            print(f"  SKIP (file not found): {fig_file}")
            continue

        slide = prs.slides[slide_idx]
        old_img = main_content_image(slide)
        if old_img is None:
            print(f"  SKIP slide {slide_idx+1}: no qualifying image found")
            continue

        # Record old position
        old_left   = old_img.left
        old_top    = old_img.top
        old_width  = old_img.width
        old_height = old_img.height

        # Maximum safe dimensions
        max_w = min(old_width,  slide_w - old_left - MARGIN)
        max_h = min(slide_h - old_top - MARGIN,
                    slide_h - old_top - MARGIN)

        # Fit new image
        new_left, new_top, new_w, new_h = fit_image(
            img_path, old_left, old_top, max_w, max_h
        )

        # Remove old image
        remove_shape(old_img)

        # Insert new image
        pic = slide.shapes.add_picture(
            str(img_path), new_left, new_top,
            width=Emu(new_w), height=Emu(new_h)
        )

        new_w_in = Emu(new_w).inches
        new_h_in = Emu(new_h).inches
        old_w_in = old_width.inches
        old_h_in = old_height.inches

        print(f"  [slide {slide_idx+1:2d}] replaced  {old_w_in:.2f}\"x{old_h_in:.2f}\"  "
              f"->  {new_w_in:.2f}\"x{new_h_in:.2f}\"  |  {fig_file}")
        replaced.append((slide_idx + 1, fig_file))

    # ── Save ─────────────────────────────────────────────────────────────
    prs.save(str(PPTX_OUT))

    # ── Summary ──────────────────────────────────────────────────────────
    print()
    print("=" * 62)
    print("DEFENCE SLIDE UPDATE SUMMARY")
    print("=" * 62)
    print(f"  Total slides:    {len(prs.slides)}")
    print(f"  Slides updated:  {len(replaced)}")
    print()

    if replaced:
        print("  Figures replaced:")
        for slide_num, fig_file in replaced:
            print(f"    slide {slide_num:2d}  {fig_file}")

    if unmatched:
        print()
        print("  Not matched (no slide found with keyword):")
        for pattern, fig_file in unmatched:
            print(f"    [{pattern}]  ->  {fig_file}")
        print("  Tip: add SLIDE_OVERRIDES[<keyword>] = <slide_num> to force placement.")

    print()
    print(f"  Saved -> {PPTX_OUT}")
    print("=" * 62)


if __name__ == "__main__":
    main()
